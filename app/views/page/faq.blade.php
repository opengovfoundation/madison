      <h1>Frequently Asked Questions</h1>
      <dl>
        <dt>What is Madison?</dt>
        <dd>Madison is a free tool that helps elected officials and everyday Americans solve problems together. Elected officials post drafts of policy documents on the site so that you can weigh in and stay informed about the issues you care about most.  Madison brings policymaking out into the open, and gives you a voice in your government and a way to hold it accountable.</dd>
      </dl>
      <dl>
        <dt>How can I use Madison to be heard on the issues I care about most?</dt>
        <dd>Government policy covers a huge range of issues that directly impact you, your family and your place of work--from where you can park your car, to where your kids go to school, to how much you pay in taxes. To find a bill on a topic you care about, simply search Madison with keywords, councilmembers’ names, or document status. Then <a href="/user/signup">sign up</a> for an acount to vote, add general comments, ask questions, and suggest changes directly to these new laws, rules and regulations. </dd>
      </dl>
      <dl>
        <dt>How do I annotate a document in Madison?</dt>
        <dd>Thanks for asking!  We’ve made you <a href="https://cloud.githubusercontent.com/assets/2349231/2661178/bd26933c-c029-11e3-94ee-09fabc347d5e.gif" target="_blank">a helpful video</a> showing you how it’s done.  (It’s super easy!)</dd>
      </dl>
      <dl>
        <dt>How can I get help with a Madison issue or question?</dt>
        <dd>
          <p>To report a technical issue or suggest new Madison features or documents to add, please use the “Feedback and Support” tab on the left-hand side of every web page.</p>
          <p>To ask questions about Madison or your user account, send us an email at sayhello@opengovfoundation.org. We’d love to hear from you!</p>
          <p>To raise a question about a document in Madison, just <a href="/user/signup">sign up</a> and add it directly to the document and the debate.</p>
        </dd>
      </dl>
      <dl>
        <dt>How can I add documents to Madison?</dt>
        <dd>Once you have signed up for Madison and have a username and password, please email us at <a href="mailto:sayhello@opengovfoundation.org">sayhello@opengovfoundation.org</a> to request an admin account in order to add new documents to Madison.</dd>
      </dl>
      <dl>
        <dt>How do I know if my Madison comments and suggestions are read by my local elected officials?</dt>
        <dd>When a document sponsor reads your comment, question or annotation, you will instantaneously receive both an email notification and a Madison notification.</dd>
      </dl>
      <dl>
        <dt>What is a verified Madison user and how do I become one?</dt>
        <dd>
          <p>A verified user is someone who has proven that they are a real human being, or a real representative of a real organization. Being a verified user gives your activity on Madison a little more weight--for example, by showing an elected official that you’re an actual constituent, or by demonstrating that you are an expert in a pertinent field.</p>
          <p>Request verification at the bottom of your profile page.</p>
        </dd>
      </dl>
      <dl>
        <dt>Who runs Madison?</dt>
        <dd>
          <p>Madison is free, <a href="https://github.com/opengovfoundation/madison" target="_blank">open-source software</a> created by <a href="http://opengovfoundation.org" target="_blank">The OpenGov Foundation</a>, a nonpartisan, nonprofit 501(c)3 based in Washington, DC. We build tools to better connect you with your government officials, help you hold them accountable, and make sure your government is working for you and your family. We just handle the technical side of Madison; any policy posted on the site comes directly from government officials or other users.</p>
        </dd>
      </dl>
      <dl>
        <dt>How can I support the growth and improvement of the Madison community?</dt>
        <dd>
          <p><a href="/user/signup">Join the Madison community!</a> Contribute your suggestions to policy documents, interact with other users, and suggest improvements and changes to the site itself.</p>
      
          <p>Keep in touch--<a href="http://opengovfoundation.us6.list-manage.com/subscribe?u=9d450bf68b3df1185fc9f62b2&id=697ad42ab6" target="_blank">sign up for news and updates on Madison</a>.</p>
      
          <p>Contact <a href="http://www.usa.gov/Contact/Elected.shtml" target="_blank">your elected officials</a>. Madison is built for democracies everywhere, at any level of government. Spread the word!</p>
      
          <p>If you’re a developer, fork <a href="https://github.com/opengovfoundation/madison" target="_blank">Madison on Github.</a></p>
        </dd>
      </dl>
      <dl>
        <dt>How can I bring Madison to my local and state government?</dt>
        <dd>Drop us a line at <a href="mailto:sayhello@opengovfoundation.org">sayhello@opengovfoundation.org</a>, and we can help you start a Madison community in your city or state.</dd>
      </dl>
